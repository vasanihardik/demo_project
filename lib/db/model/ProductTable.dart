import 'package:floor/floor.dart';

@entity
class ProductsTable {
  @primaryKey
  int? id;

  String? title;
  String? description;
  int? price;
  double? discountPercentage;
  double? rating;
  int? stock;
  String? brand;
  String? category;
  String? thumbnail;
  //List<String>? images;



  ProductsTable(
      {this.id,
      this.title,
      this.description,
      this.price,
      this.discountPercentage,
      this.rating,
      this.stock,
      this.brand,
      this.category,
      this.thumbnail});




}